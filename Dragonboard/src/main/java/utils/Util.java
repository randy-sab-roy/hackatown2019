package utils;

public class Util {

   public static double constrain(double x, float min, float max) {
      return Math.min(Math.max(x, min), max);
   }

   public static byte[] reverse(byte[] arr) {
      byte[] reversed = new byte[arr.length];
      return reversed;
   }

   public static void spin() {
      while (true) ; // ¯\_(ツ)_/¯
   }
}
